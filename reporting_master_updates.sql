UPDATE "NRFP_FINANCIALS_REPORTINGMASTER"
SET "QUERY" = 'WITH
IMPAIRMENTS_CART AS (
    SELECT A.DATA_TYPE, A.T_VERSION, A.MONTH_ABR, A.CFX, A.RFX, D.ADD16, D.ADD17, D.ADD18
    FROM "T1NFRP_PHY"."CV_UP_NFRP_ALLDATA" A
    CROSS JOIN "T1NFRP_PHY"."TBL_NFRP_DATE_CONFIG" D
    WHERE A.M_ACCOUNT_0 = ''Impairments'' AND A.T_REPORTING = ''CIB''
    AND D.FILTER_DATE = ''CURRENT_MONTH'' AND D.FILTER_TYPE = ''PYR+CYR''
),
IMPAIRMENTS_DATE AS (
    SELECT A.DATA_TYPE, A.T_VERSION, A.MONTH_ABR, A.CFX, A.RFX, D.ADD6, D.ADD16, D.ADD18
    FROM "T1NFRP_PHY"."CV_UP_NFRP_ALLDATA" A
    JOIN "T1NFRP_PHY"."TBL_NFRP_DATE_CONFIG" D ON A.E_PERIOD_DATE = D.E_PERIOD_DATE
    WHERE A.M_ACCOUNT_0 = ''Impairments'' AND A.T_REPORTING = ''CIB''
    AND D.FILTER_DATE = ''CURRENT_MONTH'' AND D.FILTER_TYPE = ''PYR+CYR''
),
YTD_ACTUALS AS (
    SELECT SUM(CFX) AS YTD_ACTUALS_CFX, SUM(RFX) AS YTD_ACTUALS_RFX
    FROM IMPAIRMENTS_CART WHERE DATA_TYPE = ''Actuals'' AND T_VERSION = ''YTD'' AND ADD16 = 0
),
VS_BUDGET AS (
    SELECT
        CASE WHEN SUM(CASE WHEN DATA_TYPE = ''Budget'' AND ADD16 = 0 THEN CFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND ADD16 = 0 THEN CFX ELSE 0 END) - SUM(CASE WHEN DATA_TYPE = ''Budget'' AND ADD16 = 0 THEN CFX ELSE 0 END))
             / ABS(SUM(CASE WHEN DATA_TYPE = ''Budget'' AND ADD16 = 0 THEN CFX ELSE 0 END)) * 100 END AS VS_BUDGET_CFX_PCT,
        CASE WHEN SUM(CASE WHEN DATA_TYPE = ''Budget'' AND ADD16 = 0 THEN RFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND ADD16 = 0 THEN RFX ELSE 0 END) - SUM(CASE WHEN DATA_TYPE = ''Budget'' AND ADD16 = 0 THEN RFX ELSE 0 END))
             / ABS(SUM(CASE WHEN DATA_TYPE = ''Budget'' AND ADD16 = 0 THEN RFX ELSE 0 END)) * 100 END AS VS_BUDGET_RFX_PCT
    FROM IMPAIRMENTS_CART WHERE DATA_TYPE IN (''Actuals'',''Budget'') AND T_VERSION = ''YTD'' AND ADD16 = 0
),
CY_PQ_ACTUALS AS (
    SELECT SUM(CFX) AS CY_PQ_ACTUALS_CFX, SUM(RFX) AS CY_PQ_ACTUALS_RFX
    FROM IMPAIRMENTS_CART WHERE DATA_TYPE = ''Actuals'' AND T_VERSION = ''QTD'' AND ADD17 = -1
),
FY_OUTLOOK AS (
    SELECT SUM(CFX) AS FY_OUTLOOK_CFX, SUM(RFX) AS FY_OUTLOOK_RFX
    FROM IMPAIRMENTS_CART WHERE DATA_TYPE = ''Outlook_PM'' AND T_VERSION = ''YTD'' AND MONTH_ABR = ''Nov'' AND ADD18 = -1
),
PY_PQ_ACTUALS AS (
    SELECT
        CASE WHEN SUM(CASE WHEN ADD17 = -4 THEN CFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN ADD17 = -1 THEN CFX ELSE 0 END) - SUM(CASE WHEN ADD17 = -4 THEN CFX ELSE 0 END))
             / ABS(SUM(CASE WHEN ADD17 = -4 THEN CFX ELSE 0 END)) * 100 END AS PY_PQ_CFX_VARIANCE,
        CASE WHEN SUM(CASE WHEN ADD17 = -4 THEN RFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN ADD17 = -1 THEN RFX ELSE 0 END) - SUM(CASE WHEN ADD17 = -4 THEN RFX ELSE 0 END))
             / ABS(SUM(CASE WHEN ADD17 = -4 THEN RFX ELSE 0 END)) * 100 END AS PY_PQ_RFX_VARIANCE
    FROM IMPAIRMENTS_CART WHERE DATA_TYPE = ''Actuals'' AND T_VERSION = ''QTD'' AND ADD17 IN (-1, -4)
),
YTD_ACTUAL_PY AS (
    SELECT
        CASE WHEN SUM(CASE WHEN T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN CFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN T_VERSION = ''YTD'' AND ADD16 = 0 THEN CFX ELSE 0 END) - SUM(CASE WHEN T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN CFX ELSE 0 END))
             / ABS(SUM(CASE WHEN T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN CFX ELSE 0 END)) * 100 END AS YTD_ACTUAL_PY_CFX_VARIANCE,
        CASE WHEN SUM(CASE WHEN T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN RFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN T_VERSION = ''YTD'' AND ADD16 = 0 THEN RFX ELSE 0 END) - SUM(CASE WHEN T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN RFX ELSE 0 END))
             / ABS(SUM(CASE WHEN T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN RFX ELSE 0 END)) * 100 END AS YTD_ACTUAL_PY_RFX_VARIANCE
    FROM IMPAIRMENTS_DATE WHERE DATA_TYPE = ''Actuals''
),
FY_OUTLOOK_PY AS (
    SELECT
        CASE WHEN SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN CFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN DATA_TYPE = ''Outlook_PM'' AND T_VERSION = ''YTD'' AND MONTH_ABR = ''Nov'' AND ADD18 = -1 THEN CFX ELSE 0 END) - SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN CFX ELSE 0 END))
             / ABS(SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN CFX ELSE 0 END)) * 100 END AS FY_OUTLOOK_PY_CFX_VARIANCE,
        CASE WHEN SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN RFX ELSE 0 END) = 0 THEN NULL
        ELSE (SUM(CASE WHEN DATA_TYPE = ''Outlook_PM'' AND T_VERSION = ''YTD'' AND MONTH_ABR = ''Nov'' AND ADD18 = -1 THEN RFX ELSE 0 END) - SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN RFX ELSE 0 END))
             / ABS(SUM(CASE WHEN DATA_TYPE = ''Actuals'' AND T_VERSION = ''MTD'' AND ADD6 = ''PY_YTD'' THEN RFX ELSE 0 END)) * 100 END AS FY_OUTLOOK_PY_RFX_VARIANCE
    FROM IMPAIRMENTS_DATE WHERE DATA_TYPE IN (''Outlook_PM'',''Actuals'')
)
SELECT A.YTD_ACTUALS_CFX, A.YTD_ACTUALS_RFX,
       B.VS_BUDGET_CFX_PCT, B.VS_BUDGET_RFX_PCT,
       C.CY_PQ_ACTUALS_CFX, C.CY_PQ_ACTUALS_RFX,
       D.FY_OUTLOOK_CFX, D.FY_OUTLOOK_RFX,
       E.PY_PQ_CFX_VARIANCE, E.PY_PQ_RFX_VARIANCE,
