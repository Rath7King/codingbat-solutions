       G.FY_OUTLOOK_PY_CFX_VARIANCE, G.FY_OUTLOOK_PY_RFX_VARIANCE
FROM YTD_ACTUALS A CROSS JOIN VS_BUDGET B CROSS JOIN CY_PQ_ACTUALS C
CROSS JOIN FY_OUTLOOK D CROSS JOIN PY_PQ_ACTUALS E
CROSS JOIN YTD_ACTUAL_PY F CROSS JOIN FY_OUTLOOK_PY G'
